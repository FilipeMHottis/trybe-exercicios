function addSaldo(saldo, add){
    let final = saldo + add;
    return final
}

function subSaldo(saldo, sub){
    let final = saldo - sub;
    return final
}

function taxa(saldo, taxa){
    let final = saldo * taxa;
    return final
}

function divSaldo(saldo, div){
    let final = saldo / div;
    return final
}